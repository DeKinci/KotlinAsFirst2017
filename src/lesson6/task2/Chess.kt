/**
 * Сложная
 *
 * Определить число ходов, за которое шахматный конь пройдёт из клетки start в клетку end.
 * Шахматный конь одним ходом вначале передвигается ровно на 2 клетки по горизонтали или вертикали,
 * а затем ещё на 1 клетку под прямым углом, образуя букву "Г".
 * Ниже точками выделены возможные ходы коня, а крестиками -- невозможные:
 *
 * .xxx.xxx
 * xxKxxxxx
 * .xxx.xxx
 * x.x.xxxx
 * xxxxxxxx
 * xxxxxxxx
 * xxxxxxxx
 * xxxxxxxx
 *
 * Если клетки start и end совпадают, вернуть 0.
 * Если любая из клеток некорректна, бросить IllegalArgumentException().
 *
 * Пример: knightMoveNumber(Square(3, 1), Square(6, 3)) = 3.
 * Конь может последовательно пройти через клетки (5, 2) и (4, 4) к клетке (6, 3).
 */
fun knightMoveNumber(start: Square, end: Square): Int {
    checkCells(start, end)

    val map = createMap()
    recursiveKnightMoves(start, map)

    return map[end.row - 1][end.column - 1]
}

fun recursiveKnightMoves(
        recent: Square,
        map: Array<IntArray>,
        counter: Int = 0
) {
    if (map[recent.row - 1][recent.column - 1] <= counter)
        return

    map[recent.row - 1][recent.column - 1] = counter

    val nextHops = getPossibleHorseHops(recent)
    for (hop in nextHops)
        recursiveKnightMoves(hop, map, counter + 1)
}

fun getPossibleHorseHops(start: Square): List<Square> {
    val horseDeltas = listOf(Pair(2, 1), Pair(1, 2), Pair(-1, 2), Pair(-2, 1),
            Pair(-2, -1), Pair(-1, -2), Pair(1, -2), Pair(2, -1))

    val result = ArrayList<Square>()
    for ((dColumn, dRow) in horseDeltas) {
        val possibleHop = Square(start.column + dColumn, start.row + dRow)
        if (possibleHop.inside())
            result.add(possibleHop)
    }

    return result.toList()
}

fun createMap(): Array<IntArray> {
    val map = Array(8) { IntArray(8) }
    for (row in map)
        row.fill(Int.MAX_VALUE)
    return map
}

fun checkCells(vararg cells: Square) {
    for (cell in cells)
        if (!cell.inside())
            throw IllegalArgumentException()
}

/**
 * Очень сложная
 *
 * Вернуть список из клеток, по которым шахматный конь может быстрее всего попасть из клетки start в клетку end.
 * Описание ходов коня см. предыдущую задачу.
 * Список всегда включает в себя клетку start. Клетка end включается, если она не совпадает со start.
 * Между ними должны находиться промежуточные клетки, по порядку от start до end.
 * Примеры:
 *
 * knightTrajectory(Square(3, 3), Square(3, 3)) = listOf(Square(3, 3))
 * здесь возможны другие варианты)
 * knightTrajectory(Square(3, 1), Square(6, 3)) = listOf(Square(3, 1), Square(5, 2), Square(4, 4), Square(6, 3))
 * (здесь возможен единственный вариант)
 * knightTrajectory(Square(3, 5), Square(5, 6)) = listOf(Square(3, 5), Square(5, 6))
 * (здесь опять возможны другие варианты)
 * knightTrajectory(Square(7, 7), Square(8, 8)) =
 *     listOf(Square(7, 7), Square(5, 8), Square(4, 6), Square(6, 7), Square(8, 8))
 *
 * Если возможно несколько вариантов самой быстрой траектории, вернуть любой из них.
 */
fun knightTrajectory(start: Square, end: Square): List<Square> {
    val trajectory = ArrayList<Square>()
    val map = createMap()

    recursiveKnightMoves(start, map)
    recursiveKnightTrajectoryFinder(end, map, trajectory)

    return trajectory.reversed()
}

fun recursiveKnightTrajectoryFinder(
        recentPosition: Square,
        map: Array<IntArray>,
        trajectory: ArrayList<Square>
) {
    trajectory.add(recentPosition)

    if (map[recentPosition.row - 1][recentPosition.column - 1] == 0)
        return

    recursiveKnightTrajectoryFinder(
            findBestHop(recentPosition, map),
            map, trajectory)
}

fun findBestHop(recentPosition: Square, map: Array<IntArray>): Square {
    var bestNextHop: Square? = null
    var bestHopMetrics = Int.MAX_VALUE

    val nextHops = getPossibleHorseHops(recentPosition)
    for (hop in nextHops)
        if (map[hop.row - 1][hop.column - 1] < bestHopMetrics) {
            bestNextHop = hop
            bestHopMetrics = map[hop.row - 1][hop.column - 1]
        }

    if (bestNextHop != null)
        return bestNextHop
    else
        throw IllegalStateException()
}